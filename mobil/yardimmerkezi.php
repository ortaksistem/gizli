
    <h1 class="title">Yardım Merkezi</h1>
    <p>&nbsp;</p>
    <p><strong><span style="font-size: 12pt;">Üyeliğimi Nasıl Yükseltebilirim?</span></strong></p>
    <p>	
Üyeliği yükseltmenin en hızlı yolu kredi kartı veya havale ile üyelik satın almaktır. Bu şekilde üyelik satın almak için Profiliniz ve menüde bulunan Üyeliğimi Yükselt linklerine tıklayarak satın almak istediğiniz üyelik tipini belirleyerek ileriki adımlara geçebilirsiniz.</p>
<p>Ödeme bilgilerini doldurup gerekli ödemeyi Kredi Kartı veya Havale ile yaptığınızda üyeliğiniz ödemesi yapılmış olan üyelik tipi şeklinde açılacak ve satın almış olduğunuz üyelik tipinin haklarını kullanmaya başlayabileceksiniz.</p>

<p><strong><span style="font-size: 12pt;">Ücretsiz Medium veya Large üyelik nasıl alabilirim?</span></strong></p>
    <p>	
	
Bunu yapmanın 2 yolu bulunmaktadır. Birincisi Puan sistemidir bu sistem ile size verilen puanları toplayarak.</p>
<p>Belli bir puana ulaştıktan sonra puanınızla üyelik alabilirsiniz. Puanların nasıl kazanılacağı Puan Nasıl Toplarım linkinde mevcuttur. 
Diğer yol ise bu sadece bayan ve de Çift üyeler için geçerlidir.GKS'li üye olarak ücretsiz yararlanabilirsiniz</p>


<p><strong><span style="font-size: 12pt;">Sohbet odalarından yararlanmak için nelere ihtiyacım var?

</span></strong></p>
    <p>Öncelikle Medium veya Large üyeliğe ihtiyacınız var.Bu iki tip üyelikten birine sahipseniz sohbete girmek için bilgisayarınızda Java yüklü olması yeterlidir...</p>

<p><strong><span style="font-size: 12pt;">	
Small üyeler neden sistemi kullanamıyorlar.</span></strong></p>
    <p>	
Small üyelik sistemi siteyi incelemeniz ve de beğendiğiniz takdirde Medium yada Large üyeliklerden alıp almamaya karar vermenizi sağlamak için vardır .Small üyelik sistemi kullanmak için değil sistemi ve işleyişini tanımak için açılan üyelik tipidir.</p>


<p><strong><span style="font-size: 12pt;">	
Small üyeler neden bu kadar kısıtlanıyor?</span></strong></p>
    <p>Small üyelik her sistemde siteyi tanıtmak amacı ile oluşturulmuş bir üyelik biçimidir ve ücretsizdir ve de sitenin bir çok özelliğinden hiç bir yerde yararlanamaz sistemi beğenirse üyeliğini yükseltir ve yararlanmak istediklerinden yararlanabilir. Small üye nin mesaj hakkı bulunmamaktadır...</p>

<p><strong><span style="font-size: 12pt;">Puan sistemi nasıl çalışıyor?</span></strong></p>
    <p>Puan sistemi sitede yapacağınız her türlü aktivite sonucunda sistemin size vereceği puanlar sayesinde çalışır. Bu puanları daha sonra kullanarak SHOP merkezimden dilediğiniz şekilde harcayabilirsiniz...</p>

<p><strong><span style="font-size: 12pt;">Profilimde resimlerim neden onaylanmadı?</span></strong></p>
    <p>Bunun bir kaç sebebi olabilir.Öncelikle onay verilecekleri henüz Editörlerimiz incelememiş olabilir ancak en sık görülen neden sizin resimlerinizden Ana resim seçmemiş olmanızdır. Ana resim seçerseniz resimlerinizde görülür</p>

<p><strong><span style="font-size: 12pt;">Neden profilimize istediğimiz resimleri yükleyemiyoruz ?</span></strong></p>
    <p>	
Burası profil ve resim bilgilerini çok ciddiye alan bir sitedir ve kesin değişmez kurallarımızdan biride size ait olmayan resimlerin eklenmesine kesinlikle izin verip alakasız resimler ile resimli profil aramalarında çıkarak sadece resimli profil arayan üyelerimizin vaktini boşa harcamanızı engellemektir.</p>
<p>Çit olmayan üyelerde yine başkaları ile olan resimlerini ekleyemez bu tip profillerde de üyelik tipi ne olursa olsun tarafımızdan silinir.</p>

<p><strong><span style="font-size: 12pt;">Mesaj yolladığım kişiler neden mesajlarımı okumuyor ?</span></strong></p>
    <p>u sorunun muhatabı mesajı attığınız kişidir bunu bizlere sormanız yanlıştır.Mesaj attığınız kişi sisteme girmiyor olabilir, mesajınızla ilgilenmiyor olabilir bunun bir garantisi yoktur...</p>

<p><strong><span style="font-size: 12pt;">Medium ve Large Üyeler Mesajlaşmalarda Nelere Dikkat Etmeliler ?</span></strong></p>
<p>Üyelik satın alarak ciddiyetinizi , kararlılığınızı, istekliliğinizi kanıtlamış kişiler olarak Medium ve Large üyeler standart üyelere mesajlarında mail adresi vererek ayrıca kendilerini riske atmaktadırlar. </p>
<p>Unutmayın siz kendinizi kanıtlamışken küçük bir bedeli ödemeden size ulaşmak isteyen kişiler bizler ne kadar çaba göster sekte profillerinde yazdıkları kişiler olmayabilir. Bu tip durumlarda karşılaşabileceğiniz sorunlardan sitemiz kesinlikle sorumlu tutulamaz</p>
<p>Mail adresi çalınmasına kadarda her türlü sorunu yaşayabilirsiniz.Unutmayın birileri ile tanışmanın en güzel yolu sizin ödediğiniz bedeli ödemiş ciddiyetlerini kanıtlamış kişilerle tanışmaktır.Bu şekilde her türlü riski minimuma indirmiş olursunuz.</p>

